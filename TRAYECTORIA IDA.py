import turtle

def dibujar_ventana():
    turtle.speed(1)
    
    
    turtle.penup()
    turtle.goto(0, 0)
    turtle.pendown()
    
    turtle.forward(25)
    turtle.left(90)
    turtle.forward(35)
    turtle.right(90)
    turtle.forward(45)
    turtle.left(90)
    turtle.forward(52)
    turtle.left(90)
    turtle.forward(85)
    turtle.right(90)
    turtle.forward(55)
    turtle.right(90)
    turtle.forward(47)
    turtle.left(90)
    turtle.forward(30)
    turtle.right(90)
    turtle.forward(35)
    
    
    turtle.hideturtle()

dibujar_ventana()
    
turtle.done()